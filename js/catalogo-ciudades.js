// Helper: ¿la cadena coincide con alguna ciudad? (para validación)
export function esCiudadValida(texto) {
  if (!texto) return false;
  const t = String(texto).trim().toLowerCase();
  return CIUDADES_COLOMBIA.some(c =>
    `${c.municipio}, ${c.departamento}`.toLowerCase() === t
    || c.municipio.toLowerCase() === t
  );
}
